export function ColorType(type: string) {
  switch (type) {
    case "bug":
      return "#93bb37";
    case "poison":
      return "#a663c3";
    case "normal":
      return "#929aa2";
    case "fighting":
      return "#d03e68";
    case "flying":
      return "#94aee1";
    case "ground":
      return "#d97b47";
    case "rock":
      return "#c9ba8d";
    case "ghost":
      return "#556ab3";
    case "steel":
      return "#528b9e";
    case "fire":
      return "#ff9f52";
    case "water":
      return "#58a3dc";
    case "grass":
      return "#5fbc5a";
    case "electric":
      return "#f5d646";
    case "psychic":
      return "#f9797d";
    case "ice":
      return "#7cd3c7";
    case "dragon":
      return "#026fc1";
    case "dark":
      return "#5c5569";
    case "fairy":
      return "#ee93e5";
    default:
      "#a6adbb";
  }
}
